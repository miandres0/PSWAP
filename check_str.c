int check_str(char *s)
{
	int	i;

	if (!s)
		return (0);
	i = 0;
	while (s[i])
	{
		if (!(s[i] == ' ' || ('0' <= s[i] && s[i] <= '9')))
		{
			if (!(s[i] == '+' || s[i] == '-'))
				return (0);
			else
			{
				i++;
				if (!('0' <= s[i] && s[i] <= '9'))
					return (0);
			}
		}
		else
			return (0);
		i++;
	}
	return (1);
}
