int check_str(char *s)
{
	int	i;
	int	r;
	
	r = 1;
	if (!s)
		return (0);
	i = 0;
	while (s[i])
	{
		if (s[i] != ' ' && !('0' < s[i] && s[i] < '9'))
		{
			if (s[i] == '+' || s[i] == '-')
			{
				if ((i != 0 && s[(i - 1)]== ' ') \
					||  !('0' < s[i + 1] && s[i + 1] < '9'))
				{
					r = 0;
					break;
				}
			}
		}
		i++;
	}
	return (r);
}